    public void importAll() throws IOException {
        fileHandle file = new fileHandle("rooms.txt");
        int ID = file.getLineNumber();
        nextID.setText(String.valueOf(ID));
        
        // Reset table
        DefaultTableModel model = (DefaultTableModel)allRoomsTable.getModel();
        model.setRowCount(0);
        
        Object[] fileRow = file.readFromFile();
        
        for (var i = 0; i < fileRow.length; i++) {
            String line = fileRow[i].toString().trim();
            String[] dataRow = line.split(", ");
            if (i == 0){
                model.setColumnIdentifiers(dataRow);
            } else {
                model.addRow(dataRow);
            }
        }
    }
    
    public void importCategorised() throws IOException {
        fileHandle file = new fileHandle("rooms.txt");
        int ID = file.getLineNumber();
        nextID.setText(String.valueOf(ID));
        
        // Reset table
        DefaultTableModel availablemodel = (DefaultTableModel)availableRoomsTable.getModel();
        DefaultTableModel reservemodel = (DefaultTableModel)reservedRoomsTable.getModel();
        availablemodel.setRowCount(0);
        reservemodel.setRowCount(0);
        
        Object[] fileRow = file.readFromFile();
        
        for (var i = 0; i < fileRow.length; i++) {
            String line = fileRow[i].toString().trim();
            String[] dataRow = line.split(", ");
            if (i == 0){
                availablemodel.setColumnIdentifiers(dataRow);
                reservemodel.setColumnIdentifiers(dataRow);
            } else if (dataRow[4].equals("True")){
                availablemodel.addRow(dataRow);
            } else if (dataRow[4].equals("False")){
                reservemodel.addRow(dataRow);
            }
        }
    }
    
