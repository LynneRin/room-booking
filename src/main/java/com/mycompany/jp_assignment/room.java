    private void addRoomBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addRoomBtnActionPerformed
        //Get Room number, room view, room type (user input)
        String roomNum = roomNumberInput.getText();
        String roomView = roomViewInput.getSelectedItem().toString();
        String roomType = roomTypeInput.getSelectedItem().toString();
        String id = nextID.getText();

        String[] data = {id, roomNum, roomView, roomType, "True"};

        fileHandle insert = new fileHandle("rooms.txt");

        if (insert.writeToFile(data)) {
            JOptionPane.showMessageDialog(this, "New room data has been added successfully.", "Insertion Successfull", JOptionPane.INFORMATION_MESSAGE);
            try {
                importAll();
            } catch (IOException ex) {
                Logger.getLogger(room.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                importCategorised();
            } catch (IOException ex) {
                Logger.getLogger(room.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "An error has occured while inserting!\nPlease try again.", "Insertion Failed", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_addRoomBtnActionPerformed

